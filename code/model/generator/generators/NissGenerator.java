package model.generator.generators;

import gui.controller.tabs.NissGeneratorTabController;
import model.generator.Generator;

public class NissGenerator extends Generator {

    private NissGeneratorTabController ngtc;
    private String[] values;

    public NissGenerator(NissGeneratorTabController ngtc) {

        this.setName("Niss Generator");
        setNgtc(ngtc);
    }

    // --- Methods --------------------------------------------------------------------------------

    @Override
    public String generateLoot() {

        String weaponType       = ngtc.getWeaponType();
        String classification   = ngtc.getClassification();
        String quality          = ngtc.getQuality();
        String damage           = ngtc.getDamage();
        String hitchance        = ngtc.getHitchance();
        String parade           = ngtc.getParade();
        String magazine         = ngtc.getMagazine();
        String addition         = ngtc.getAddition();

        return "Cool loot has been generated by niss :)";
    }


    // --- Getter and Setter ----------------------------------------------------------------------

    private void setNgtc(NissGeneratorTabController ngtc) {
        this.ngtc = ngtc;
    }

    private NissGeneratorTabController getNgtc() {
        return this.ngtc;
    }
}